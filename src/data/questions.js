/** Build a single-word hangman question from a lesson */
export function buildHangmanQuestions(lesson = null) {
  const targetLesson = lesson ?? VOCAB_BOOKS[0].lessons[0];
  return shuffle(targetLesson.words).map(makeHangmanQuestion);
}

