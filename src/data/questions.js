import { VOCAB_BOOKS } from "./vocab";

/* ─── Helpers ────────────────────────────────────────────────────── */

/** Fisher-Yates shuffle — unbiased */
function shuffle(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

/** Pick `n` random items from an array, excluding a specific value */
function pickRandom(arr, n, exclude = null) {
  return shuffle(arr.filter(x => x !== exclude)).slice(0, n);
}

/** Build a scrambled letter bank for the spell game */
function makeLetterBank(word) {
  const correct = word.split("");
  // Add 3 decoy letters that aren't already in the word
  const decoys = "abcdefghijklmnopqrstuvwxyz"
    .split("")
    .filter(l => !correct.includes(l));
  const extras = pickRandom(decoys, 3).map(l => l);
  return shuffle([...correct, ...extras]);
}

/* ─── Question builders (one per game type) ──────────────────────── */

function makePictureQuestion(wordObj, allWords) {
  // Distractor options are other words in the lesson (by emoji/label)
  const distractors = pickRandom(allWords, 3, wordObj);
  const options = shuffle([
    { value: wordObj.word, emoji: wordObj.image, label: wordObj.word },
    ...distractors.map(w => ({ value: w.word, emoji: w.image, label: w.word })),
  ]);
  return {
    type: "picture",
    word: wordObj.word,
    meaning: wordObj.meaning,
    hint: wordObj.sentence,
    correct: wordObj.word,
    praise: `Yes! "${wordObj.word}" means ${wordObj.meaning}.`,
    explanation: wordObj.sentence,
    options,
  };
}

function makeDefinitionQuestion(wordObj, allWords) {
  const sentence = wordObj.sentence;
  const wordIdx  = sentence.toLowerCase().indexOf(wordObj.word.toLowerCase());
  const before   = wordIdx >= 0 ? sentence.slice(0, wordIdx) : "";
  const after    = wordIdx >= 0 ? sentence.slice(wordIdx + wordObj.word.length) : sentence;

  const distractorMeanings = pickRandom(allWords, 3, wordObj).map(w => w.meaning);
  const options = shuffle([wordObj.meaning, ...distractorMeanings]);

  return {
    type: "definition",
    word: wordObj.word,
    sentenceParts: { before, after },
    correct: wordObj.meaning,
    praise: `Exactly! "${wordObj.word}" means ${wordObj.meaning}.`,
    explanation: `Think of the sentence: "${sentence}"`,
    options,
  };
}

function makeListenQuestion(wordObj, allWords) {
  // Three distractors: similar-length words from the lesson or slight variants
  const distractors = pickRandom(allWords, 3, wordObj).map(w => w.word);
  return {
    type: "listen",
    word: wordObj.word,
    correct: wordObj.word,
    praise: `Lovely listening! "${wordObj.word}" means ${wordObj.meaning}.`,
    explanation: `The word was "${wordObj.word}" — ${wordObj.meaning}.`,
    options: shuffle([wordObj.word, ...distractors]),
  };
}

function makeSpellQuestion(wordObj) {
  return {
    type: "spell",
    meaning: wordObj.meaning,
    correct: wordObj.word,
    correctLabel: wordObj.word,
    praise: `Spelled beautifully! A ${wordObj.word} person is ${wordObj.meaning}.`,
    explanation: `The word "${wordObj.word}" means ${wordObj.meaning}.`,
    letters: makeLetterBank(wordObj.word),
  };
}

/**
 * The fill question is AI-generated at runtime.
 * We store the word data here; FillBlankGame calls the API to
 * generate and evaluate a contextual sentence on the fly.
 */
function makeFillQuestion(wordObj, allWords) {
  const distractors = pickRandom(allWords, 3, wordObj).map(w => w.word);
  return {
    type: "fill",
    word: wordObj.word,
    meaning: wordObj.meaning,
    sentence: wordObj.sentence,   // seed sentence for context
    correct: wordObj.word,
    praise: `Beautiful! "${wordObj.word}" fits perfectly — it means ${wordObj.meaning}.`,
    explanation: `"${wordObj.word}" means ${wordObj.meaning}. ${wordObj.sentence}`,
    // Fallback options in case the AI call fails
    options: shuffle([wordObj.word, ...distractors]),
    // sentenceParts will be generated by FillBlankGame via Claude
    sentenceParts: null,
  };
}

/* ─── Main export ────────────────────────────────────────────────── */

/**
 * Builds one round of questions from the current lesson words.
 * Picks 5 words and assigns one question type to each.
 */
export function buildQuestions(lesson = null) {
  // Default to first lesson if none specified
  const targetLesson = lesson ?? VOCAB_BOOKS[0].lessons[0];
  const words = targetLesson.words;

  // We need at least 4 words; pick up to 5
  const picked = shuffle(words).slice(0, Math.min(5, words.length));

  // Assign question types round-robin so each lesson has variety
  const types = shuffle(["picture", "definition", "listen", "spell", "fill"]);

  return picked.map((wordObj, i) => {
    const type = types[i % types.length];
    switch (type) {
      case "picture":    return makePictureQuestion(wordObj, words);
      case "definition": return makeDefinitionQuestion(wordObj, words);
      case "listen":     return makeListenQuestion(wordObj, words);
      case "spell":      return makeSpellQuestion(wordObj);
      case "fill":       return makeFillQuestion(wordObj, words);
      default:           return makeDefinitionQuestion(wordObj, words);
    }
  });
}

/* ── Hangman question builder ────────────────────────────────────── */
export function makeHangmanQuestion(wordObj) {
  return {
    type:         "hangman",
    word:         wordObj.word,
    meaning:      wordObj.meaning,
    correct:      wordObj.word,
    correctLabel: wordObj.word,
    praise:       `Well done! You guessed "${wordObj.word}" — ${wordObj.meaning}.`,
    explanation:  `"${wordObj.word}" means ${wordObj.meaning}. ${wordObj.sentence ?? ""}`,
  };
}

/** Build a single-word hangman question from a lesson */
export function buildHangmanQuestions(lesson = null) {
  const targetLesson = lesson ?? VOCAB_BOOKS[0].lessons[0];
  return shuffle(targetLesson.words).map(makeHangmanQuestion);
}

/* ─── Single question dispatcher ────────────────────────────────── */
function makeQuestion(type, wordObj, allWords) {
  switch (type) {
    case "picture":    return makePictureQuestion(wordObj, allWords);
    case "definition": return makeDefinitionQuestion(wordObj, allWords);
    case "listen":     return makeListenQuestion(wordObj, allWords);
    case "spell":      return makeSpellQuestion(wordObj);
    case "fill":       return makeFillQuestion(wordObj, allWords);
    case "hangman":    return makeHangmanQuestion(wordObj);
    default:           return makeDefinitionQuestion(wordObj, allWords);
  }
}

/**
 * Build exactly `count` questions of a given type from a lesson.
 * Words are cycled if the lesson has fewer words than `count`.
 *
 * type = "mix"  → one question per game type, varied words
 * type = other  → all questions of that type, varied words
 */
export function buildTypedQuestions(lesson = null, type = "mix", count = 5) {
  const targetLesson = lesson ?? VOCAB_BOOKS[0].lessons[0];
  const words = targetLesson.words;

  // Pick `count` unique words at random (no repeats within a session)
  const picked = shuffle(words).slice(0, count);

  if (type === "mix") {
    const mixTypes = shuffle(["definition", "listen", "spell", "fill", "hangman"]);
    return picked.map((wordObj, i) =>
      makeQuestion(mixTypes[i % mixTypes.length], wordObj, words)
    );
  }

  if (type === "hangman") {
    return picked.map(wordObj => makeHangmanQuestion(wordObj));
  }

  return picked.map(wordObj => makeQuestion(type, wordObj, words));
}
